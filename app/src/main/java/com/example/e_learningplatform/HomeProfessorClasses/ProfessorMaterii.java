package com.example.e_learningplatform.HomeProfessorClasses;

public class ProfessorMaterii {

    String title;
    int image;

    public ProfessorMaterii(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }


}
